#logical operators 
A = 10
B = 20
C = 0 and 20 
D = 5 > 4 and 3 > 2
print(C)
print(D)
E = 5 > 4 or 3 > 2
print(E)
F = not(5 > 4 and 3 > 2)
print(F)
#comparison operators
G = 5 > 4
H = 5 < 4
I = 5 == 4
J = 5 != 4
print(G)
print(H)
print(I)
print(J)
#identity operators
K = 5 is 5
L = 5 is not 5
print(K)
print(L)
#membership operators
M = 'a' in 'programming'
N = 'm' in 'programming'
O = 'a' not in 'programming'
P = 'm' not in 'programming'
print(M)
print(N)
print(O)
print(P)

